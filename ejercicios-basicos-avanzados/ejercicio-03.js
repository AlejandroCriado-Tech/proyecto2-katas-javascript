// Ejercicio 3

// 3.1 - Multiplicación
console.log(10 * 5); // 50

// 3.2 - División
console.log(10 / 2); // 5

// 3.3 - Resto (módulo)
console.log(15 % 9); // 6

// 3.4 - Operador de asignación que resulta en o = 15 (p = 10, j = 5)
let p = 10;
let j = 5;
let o = p;
o += j; // o = o + j → 10 + 5 = 15
console.log(o); // 15

// 3.5 - Operador de asignación que resulta en i = 50 (c = 10, m = 5)
let c = 10;
let m = 5;
let i = c;
i *= m; // i = i * m → 10 * 5 = 50
console.log(i); // 50
